
function toggle() {
    let hamMenu = document.querySelector('.ham-menu');
    hamMenu.classList.toggle('hidden'); 
  }
  cross = document.querySelector('.hamMenu');

  if (document.cross.display.style.display = "none") {
    document.cross.display.style.display = "block"
  }

  else  {
    document.cross.display.style.display = "block"
  }